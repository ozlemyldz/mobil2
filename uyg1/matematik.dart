class matematik {
  double pi = 3.14;
  /* double yerine static const komutu ile de olabilir.*/

  double topla(double sayi1, double sayi2) {
    return sayi1 + sayi2;
  }

  double cikarma(double sayi1, double sayi2) {
    return sayi1 - sayi2;
  }

  double carpma(double sayi1, double sayi2) {
    return sayi1 * sayi2;
  }

  double bolme(double sayi1, double sayi2) {
    return sayi1 / sayi2;
  }
}
